public class Semestre 
{
	private int id;
	private String codigo;

	public Semestre()
	{
		this.codigo = new String();
	}
	public int getId() 
	{
		return id;
	}
	public String getCodigo() 
	{
		return codigo;
	}
	public void setCodigo(String codigo) 
	{
		this.codigo = codigo;
	}
}
